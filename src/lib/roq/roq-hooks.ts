export function useFavoriteFindMany<
  T extends Prisma.favoriteFindManyArgs,
  R extends GetFindResult<Prisma.$favoritePayload<DefaultArgs>, T>[],
>(
  args?: Prisma.SelectSubset<T, Prisma.favoriteFindManyArgs>,
  options?: RequestOptions,
  swrOptions?: SWRRequestOptions<R>,
) {
  const roq = useRoqClient();
  const key = JSON.stringify(['useFavoriteFindMany', args || {}]);
  return useSWR<R, Error>(
    key,
    async () => {
      const result = await roq.favorite.findMany(args, options);
      return result as R;
    },
    {
      ...swrOptions,
      fallbackData: swrOptions?.initialData ?? swrOptions?.fallbackData,
    },
  );
}

